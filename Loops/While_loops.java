
public class While_loops {
    public static void main(String[] args) {
        // While loop
        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i++;
        }

        // Do-while loop
        int j = 0;
        do {
            System.out.println(j);
            j++;
        } while (j < 5);
    }
}
